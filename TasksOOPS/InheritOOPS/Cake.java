package TasksOOPS.InheritOOPS;

public class Cake extends Recipe {
	public void getIngredients() {
		System.out.println("---------Ingredients for Cake---------");
		System.out.println("1 ½ cups (180g) all-purpose flour (maida)\n"
				+ "\n"
				+ "1 cup (200g) sugar (powdered for easy mixing)\n"
				+ "\n"
				+ "½ cup (120ml) vegetable oil or melted butter\n"
				+ "\n"
				+ "½ cup (120ml) milk (room temperature)\n"
				+ "\n"
				+ "2 eggs (or ½ cup thick curd for eggless)\n"
				+ "\n"
				+ "1 ½ tsp baking powder\r\n"
				+ "\n"
				+ "½ tsp baking soda\n"
				+ "\n"
				+ "1 tsp vanilla extract\n"
				+ "\n"
				+ "A pinch of salt\n"
				);
	}
	public void getInstruction() {
		System.out.println("---------Instruction for Cake---------");
		System.out.println("Preheat Oven:\n"
				+ "\n"
				+ "Set oven to 180°C (350°F). Grease a cake tin and line it with butter paper or dust it with flour.\n"
				+ "\n"
				+ "Mix Dry Ingredients:\n"
				+ "\n"
				+ "In a bowl, sift flour, baking powder, baking soda, and salt. Keep aside.\n"
				+ "\n"
				+ "Mix Wet Ingredients:\n"
				+ "\n"
				+ "In another bowl, beat eggs (or curd), sugar, and vanilla until creamy.\n"
				+ "\n"
				+ "Add oil and milk, and mix well.\n"
				+ "\n"
				+ "Combine:\n"
				+ "\n"
				+ "Gradually add the dry ingredients into the wet mixture. Mix gently with a spatula to avoid over-mixing.\n"
				+ "\n"
				+ "Bake:\n"
				+ "\n"
				+ "Pour the batter into the prepared tin.\n"
				+ "\n"
				+ "Bake at 180°C (350°F) for 30–35 minutes or until a toothpick inserted in the center comes out clean.\n"
				+ "\n"
				+ "Cool & Serve:\n"
				+ "\n"
				+ "Let it cool for 10–15 minutes.\n"
				+ "\n"
				+ "Remove from the tin, slice, and serve plain or with frosting.");
	}
	

}
